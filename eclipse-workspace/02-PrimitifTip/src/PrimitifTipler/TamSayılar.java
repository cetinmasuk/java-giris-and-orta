package PrimitifTipler;

public class TamSayýlar {
	public static void main(String[] args) {

/* 
 * Primitif Tipler hafýzada belirtldiði alan kadar yer kaplar.
 * 
 * Tam Sayýlar: byte < short < int <long
 * 
 * Ondalýklý Sayýlar: float < double
 * 
 * Tek karakter: char(%,&,a-z,0-9)
 * 
 * Durum : Boolen (true, false )
 * 
 */
		
		
	/*
	 * byte hafýzada 8 bit'lik yer kaplar.
	 * -127  ile +127  deðer alabilir. 
	 */
		byte bsayi = 50; 
		
		
		
		/* 
		 *  short hazýfa da 32 bit yer kaplar.
		 *  iþaretli sayýdýr ayný zamandýr. 
		 *  
		 *  -2^31 ile +2^31
		 *  
		 */
		System.out.println(bsayi);
		short ssayi = 120; 
		
		
		
		
		 /*
		  * int hafýzada 64 bit yer kaplar.
		  * en yaygýn kullanýlan tiptir.
		  * 
		  * -2^63 ile +2^63 arasýnda. 
		  */
		int isayi = 1500; 
		
		
	}

}
