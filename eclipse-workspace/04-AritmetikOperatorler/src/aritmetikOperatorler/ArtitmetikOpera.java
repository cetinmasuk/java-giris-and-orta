package aritmetikOperatorler;

public class ArtitmetikOpera {
	
	public static void main(String[] args) {
		
		
		
		int sayi1 = 11 ; 
		
		int sayi2 = 91; 
		
		//+ operatörü  
		
		 int toplam = sayi1 + sayi2; 
		 
		 System.out.println("Tolman: "+ toplam);
		 
		 // - Fark operatörü 
		 
		 int fark = sayi1 - sayi2; 
		 
		 System.out.println("Fark: " + fark);
		 
		 // * Çarpma Operatörü 
		 
		 int carp = sayi1 * sayi2; 
		 
		 System.out.println("Çarpma: "+carp);
		 
		 // / Bölme operatörü 
		  
		float bolme = (float)sayi1/ sayi2 ; 
		System.out.println("Bölme:"+bolme );
		
		// % Mod Alma iþlemi 
		
		int  mod = sayi1 % sayi2; 
		
		System.out.println("Mod Kalan: " + mod);
		  
		
		
	}

}
