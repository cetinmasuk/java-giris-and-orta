package mathMetodlarý;



public class MathMetodlarý {
	public static void main(String[] args) {
		int a = 0, b = 0;
		System.out.println(Math.PI); //Pi Hesaplama.
		System.out.println(Math.E);// Ln  hesaplamada kullanýlýr.
		System.out.println(Math.abs(a));//Mutlak Deðer hesaplama.
		System.out.println(Math.ceil(a)); // Yukarýya yuvarlama yapar. 
		System.out.println(Math.floor(a));//Aþaðýya yuvarlama.
		
		
		System.out.println(Math.pow(a,b));// Üslü sayý hesaplama.
		System.out.println(Math.sqrt(a));//Karakök almayý saðlar. 
		System.out.println(Math.max(a,b)); //Büyük olan sayýyý yazdýrýr.
		System.out.println(Math.min(a,b)); //Küçük olan sayýyý yazdýrma.
		
		
	}
}

	


